package ru.dz.shipMaster.data.misc;

public class CommunicationsException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3767138218085210871L;

	public CommunicationsException() {
		super();
	}

	public CommunicationsException(String message, Throwable cause) {
		super(message, cause);
	}

	public CommunicationsException(String message) {
		super(message);
	}

	public CommunicationsException(Throwable cause) {
		super(cause);
	}

}
